
public class Ejemplo04 {

	public static void main(String[] args) {

		// Vraiables decimales.

		float pi = 3.1416F; // Siempre F al final.

		double salario = 2000.55;

		System.out.println("El número pi es " + pi);
		System.out.println("Mi sueldo es " + salario + " euros.");

		// Variables de tipo crácter.

		char letraDni = 'H';

		char divisionPartidoFutbol = '2';

		System.out.println("La letra de mi DNI es " + letraDni + ".");
		System.out.println("El partido era de " + divisionPartidoFutbol + "ª división.");

	}

}
