
public class Ejemplo11 {

	public static void main(String[] args) {
		
		// Carácter de control \n
		
		System.out.println("Hay siete tipos de datos " 
				+ "primitivos \nen Java.\n\n\n");
		
		System.out.println("En primer lugar están los booleanos...");
		
		
		// Carácter de control \t
		
		System.out.println("\nNombre completo\tEdad");
		
		System.out.println("-------------- \t----");
		
		System.out.println("Jaume Aguiló \t22");
		
		System.out.println("Higinio Aguiló \t19");
		
		System.out.println("Gabriel Aguiló \t57");
		
		
		// Otros carácteres de control \", \' y \\
		System.out.println("\nPara hacer una conversión \"explícita\" hay que...");
		
		System.out.println("Los carácteres de control empiezan por \\.");
	}

}
