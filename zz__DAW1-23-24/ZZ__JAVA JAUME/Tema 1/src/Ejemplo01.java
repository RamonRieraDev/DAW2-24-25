
public class Ejemplo01 {

	public static void main(String[] args) {
		
		//Tipo booleano.
		boolean comprar = false;
		
		//Tipo de enteros.
		
		byte alumnos = 26;
		
		short numPeliculas = 1000;
		
		int habitantes = 700000;
		
		long numCuenta = 984392352524L; //Ponemos "L" poeqie sino lo detecta como un "int".
		
		System.out.println("Número de cuenta: " + habitantes);
		
		//Tipos de decimales.
		
		float temperatura = 20.5F;
		
		double precio = 12000.99;
		
		System.out.println("Precio de la camiseta: " + precio);
		
		//Tipo de char.
		
		char defcon = '2';
		char letraDNI = 'E';
		
		//Tipo de cadena de carácteres, que no es un tipo primitivo
		
		String nombre = "Jaume Aguiló";
	}

}
